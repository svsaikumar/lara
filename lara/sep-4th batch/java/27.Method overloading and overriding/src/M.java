class M 
{
	public static void main(String[] args) 
	{
		System.out.println("main(String[])");
	}
	public static void main(String args) 
	{
		System.out.println("main(String)");
	}
	public static void main() 
	{
		System.out.println("main()");
	}
}
//main method got overlaoded 3 times.we can overlaod main method
//in all 3 times over loading,signature is different which follows the overlaoding rule