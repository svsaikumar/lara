class E 
{
	static int i = 5;//while declaring only we can initialize
	public static void main(String[] args) 
	{
		System.out.println(i);

		i = 10;//modifying or re-intializing or re-assigning of i
		System.out.println(i);
	}
	
}
//if any global variable is initialized while declaring itself then it is called as a  initializer(not for local variable)
